                    <h3> A emissão de gases poluentes por veículos a combustão interna é uma das principais fontes de poluição atmosférica nas áreas urbanas. Esses veículos, que utilizam combustíveis fósseis como gasolina, diesel e etanol, liberam diversos compostos prejudiciais ao meio ambiente, como monóxido de carbono (CO), óxidos de nitrogênio (NOx) e hidrocarbonetos (HC). Esses poluentes contribuem diretamente para o aumento da poluição do ar, afetando tanto a qualidade do ar que respiramos quanto o equilíbrio natural do ecossistema.
                        <br> <br>Além do impacto na qualidade do ar, os gases emitidos pelos veículos a combustão são grandes responsáveis pela intensificação do efeito estufa. O dióxido de carbono (CO₂), um dos subprodutos da queima de combustíveis fósseis, é um gás de efeito estufa que contribui para o aquecimento global, uma vez que ele se acumula na atmosfera e impede que o calor irradiado pela Terra escape para o espaço. Esse acúmulo de calor altera padrões climáticos, promovendo eventos climáticos extremos e colocando em risco a biodiversidade e os recursos naturais.
                        <br><br>
