        <p class="texto">Claro que não tratamos apenas de gás de cozinha, eletricidade e cigarro... até porque, em média, a emissão de gases poluentes desses três juntos ainda é menor em relação à emissão que um veículo a gasolina comum emite. Pensando nessa informação importantíssima, fizemos também a calculadora de veículos! No parágrafo abaixo falamos um pouco sobre o quão grave é a emissão de gases poluentes por veículos como moto, carro e caminhão:
        <br>  
        A emissão de gases poluentes por veículos a combustão interna é uma das principais fontes de poluição atmosférica nas áreas urbanas. Esses veículos, que utilizam combustíveis fósseis como gasolina, diesel e etanol, liberam diversos compostos prejudiciais ao meio ambiente, como monóxido de carbono (CO), óxidos de nitrogênio (NOx) e hidrocarbonetos (HC). Esses poluentes contribuem diretamente para o aumento da poluição do ar, afetando tanto a qualidade do ar que respiramos quanto o equilíbrio natural do ecossistema.
        <br>
        Além do impacto na qualidade do ar, os gases emitidos pelos veículos a combustão são grandes responsáveis pela intensificação do efeito estufa. O dióxido de carbono (CO₂), um dos subprodutos da queima de combustíveis fósseis, é um gás de efeito estufa que contribui para o aquecimento global, uma vez que ele se acumula na atmosfera e impede que o calor irradiado pela Terra escape para o espaço. Esse acúmulo de calor altera padrões climáticos, promovendo eventos climáticos extremos e colocando em risco a biodiversidade e os recursos naturais.
        <br>
        Por fim, os poluentes emitidos pelos veículos a combustão causam impactos significativos à saúde humana. A exposição prolongada a partículas e gases tóxicos no ar pode agravar ou desencadear problemas respiratórios, como asma e bronquite, além de aumentar o risco de doenças cardiovasculares. Em áreas urbanas com alto tráfego, onde a concentração de poluentes é maior, esses riscos são mais elevados, impactando especialmente crianças, idosos e pessoas com condições de saúde preexistentes.</p>
